// ── Cohort heatmap — client-side metric selector ──────────────────────────────
// The /v1/analytics/cohort-retention endpoint takes NO params (it returns the whole-
// history acquisition curve), so there is no server-side metric to select. Instead we
// let the operator choose which engine-derived figure SHADES the heatmap. Both options
// are EXACT integer/BigInt percentages (no floats, money never blended, never /100):
//   • repeat_order_share — (lifetime orders − cohort size) ÷ lifetime orders, the
//     share of a cohort's orders that were repeat (2nd+) purchases — the come-back signal.
//   • value_share        — a cohort's share of total lifetime realized value (bigint minor units).
// Null → an honest em-dash in the cell (never a fabricated 0).
type CohortMetricKey = 'repeat_order_share' | 'value_share';

interface CohortMetricDef {
  key: CohortMetricKey;
  /** "Shade by" selector label. */
  label: string;
  /** Heatmap column header. */
  columnLabel: string;
  /** Screen-reader caption for the grid. */
  caption: string;
}

const COHORT_METRICS: CohortMetricDef[] = [
  {
    key: 'repeat_order_share',
    label: 'Repeat-order share',
    columnLabel: 'Repeat %',
    caption:
      'Cohort retention heatmap — share of each cohort’s lifetime orders that were repeat (2nd-or-later) purchases.',
  },
  {
    key: 'value_share',
    label: 'Value share',
    columnLabel: 'Value %',
    caption: 'Cohort retention heatmap — each cohort’s share of total lifetime realized value.',
  },
];

/**
 * Exact percentage (0..100) for one cohort under the selected metric, or null when it
 * is honestly undefined (→ em-dash). All math is integer/BigInt — the inputs are bigint
 * counts / bigint minor-unit money, scaled by 10000 then read back as 2dp. No floats,
 * no blended currencies, no /100.
 */
function cohortMetricValue(
  metric: CohortMetricKey,
  row: AnalyticsCohortRetentionRow,
  totalValueMinor: bigint,
): number | null {
  if (metric === 'repeat_order_share') {
    const customers = BigInt(row.cohort_size || '0');
    const orders = BigInt(row.cohort_orders || '0');
    if (orders <= 0n) return null; // no orders → nothing to measure
    const repeat = orders > customers ? orders - customers : 0n;
    return Number((repeat * 10000n) / orders) / 100;
  }
  // value_share
  const value = BigInt(row.cohort_value_minor || '0');
  if (totalValueMinor <= 0n) return null;
  return Number((value * 10000n) / totalValueMinor) / 100;
}

/** Heatmap (rows = cohort, shaded by the chosen exact metric) + the exact-figures table. */
function CohortRetentionPanel({ data }: { data: CohortHasData }) {
  const [metricKey, setMetricKey] = useState<CohortMetricKey>('repeat_order_share');
  const metric = COHORT_METRICS.find((m) => m.key === metricKey) ?? COHORT_METRICS[0];

  // Newest cohort first — matches the exact-figures table ordering below.
  const rows = [...data.cohorts].sort((a, b) => (a.cohort_month < b.cohort_month ? 1 : -1));
  const totalValueMinor = sumBig(rows, (r) => r.cohort_value_minor);

  const heatmapRows: CohortHeatmapRow[] = rows.map((r) => ({
    label: r.cohort_month,
    size: Number(BigInt(r.cohort_size || '0')),
    cells: [{ period: 0, value: cohortMetricValue(metric.key, r, totalValueMinor) }],
  }));

  return (
    <div className="space-y-5">
      <div className="flex flex-wrap items-center gap-2" role="group" aria-label="Shade heatmap by metric">
        <span className="text-xs font-medium text-muted-foreground">Shade by</span>
        {COHORT_METRICS.map((m) => {
          const active = m.key === metricKey;
          return (
            <Button
              key={m.key}
              type="button"
              variant={active ? 'default' : 'outline'}
              size="sm"
              aria-pressed={active}
              onClick={() => setMetricKey(m.key)}
            >
              {m.label}
            </Button>
          );
        })}
      </div>

      <CohortHeatmap
        rows={heatmapRows}
        periodLabels={[metric.columnLabel]}
        cohortHeader="Cohort"
        sizeHeader="New customers"
        caption={metric.caption}
        data-testid="cohort-retention-heatmap"
      />

      <div className="space-y-2">
        <p className="text-sm font-medium text-muted-foreground">Exact figures</p>
        <CohortTable data={data} />
      </div>
    </div>
  );
}

/** Largest orders-per-customer in the set → scales the inline repeat-signal bars. */
function maxOpc(rows: CohortHasData['cohorts']): number {
  return rows.reduce((m, r) => {
    const v = r.orders_per_customer != null ? Number(r.orders_per_customer) : 0;
    return v > m ? v : m;
  }, 0);
}

function CohortTable({ data }: { data: CohortHasData }) {
  // Newest cohort first.
  const rows = [...data.cohorts].sort((a, b) => (a.cohort_month < b.cohort_month ? 1 : -1));
  const scale = maxOpc(rows) || 1;

  return (
    <Table>
      <TableHeader>
        <TableRow>
          <TableHead>Cohort</TableHead>
          <TableHead className="text-right">New customers</TableHead>
          <TableHead className="text-right">Lifetime orders</TableHead>
          <TableHead>Orders / customer</TableHead>
          <TableHead className="text-right">Lifetime value</TableHead>
        </TableRow>
      </TableHeader>
      <TableBody>
        {rows.map((r) => {
          const opc = r.orders_per_customer != null ? Number(r.orders_per_customer) : null;
          const widthPct = opc != null ? Math.max(4, Math.min(100, (opc / scale) * 100)) : 0;
          return (
            <TableRow key={r.cohort_month}>
              <TableCell className="font-medium text-foreground">{r.cohort_month}</TableCell>
              <TableCell className="text-right tabular-nums">
                {Number(BigInt(r.cohort_size)).toLocaleString('en-IN')}
              </TableCell>
              <TableCell className="text-right tabular-nums">
                {Number(BigInt(r.cohort_orders)).toLocaleString('en-IN')}
              </TableCell>
              <TableCell>
                <div className="flex items-center gap-2">
                  <span className="w-10 tabular-nums text-foreground">
                    {r.orders_per_customer ?? '—'}
                  </span>
                  <div className="h-2 flex-1 overflow-hidden rounded bg-muted" aria-hidden="true">
                    <div className="h-full bg-foreground/60" style={{ width: `${widthPct}%` }} />
                  </div>
                </div>
              </TableCell>
              <TableCell className="text-right tabular-nums">
                {formatMoneyDisplay(r.cohort_value_minor, r.currency_code as CurrencyCode)}
              </TableCell>
            </TableRow>
          );
        })}
      </TableBody>
    </Table>
  );
}

