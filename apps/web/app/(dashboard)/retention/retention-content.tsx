/**
 * RetentionContent — Tab #7 "Do customers come back?".
 *
 * Answers the retention question from the existing data layer:
 *   - Repeat-purchase rate — share of customers with 2+ orders (engine-derived, exec metrics).
 *   - Acquisition-cohort curve — for each cohort-month: size, lifetime orders + value, and
 *     orders-per-customer (the repeat signal), from gold_cohorts via the metric registry.
 *
 * GAP this slice CLOSED (thin): the BFF endpoint /v1/analytics/cohort-retention + the gold_cohorts
 * mart already existed server-side, but the WEB client had no getCohortRetention fn and no hook.
 * Added analyticsApi.getCohortRetention + useCohortRetention (no new backend) — see lib/api/client.ts
 * + lib/hooks/use-analytics.ts.
 *
 * GENUINE GAP still open (flagged, NOT faked → EmptyState + openItem): "time-to-2nd-purchase"
 * (median days between a customer's 1st and 2nd order) is exposed by NO existing endpoint and would
 * need a new backend aggregate over the order spine. We do not fabricate a histogram.
 *
 * Honesty: money is bigint minor units + currency_code, formatted ONLY via formatMoneyDisplay; ratios
 * (orders_per_customer, repeat_rate_pct) come straight from the engine — never re-divided with floats
 * in the client; counts summed across cohorts use BigInt. FreshnessBadge now reads a REAL relative time
 * from the served_at (generated_at) the cohort/exec endpoints return — never a fabricated "just now".
 * EmptyState on no_data — never a fake zero.
 */

